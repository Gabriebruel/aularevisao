function executarVariaveis() {
    let texto = "Gabriel";
    let numero = 10;
    let decimal = 5.5;
    let ativo = true;

    alert("Valor: " + texto + " | Tipo: " + typeof texto);
    alert("Valor: " + numero + " | Tipo: " + typeof numero);
    alert("Valor: " + decimal + " | Tipo: " + typeof decimal);
    alert("Valor: " + ativo + " | Tipo: " + typeof ativo);

    console.log(texto, typeof texto);
    console.log(numero, typeof numero);
    console.log(decimal, typeof decimal);
    console.log(ativo, typeof ativo);
}